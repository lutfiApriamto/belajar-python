a = 10
b = 3
# operasi aritmatika pertambahan
hasil = a + b
print( a, " + ", b, " = ", hasil )
# operasi aritmatika PENGURANGAN
hasil_Kurang = a - b
print( a, " - ", b, " = ", hasil_Kurang )
# operasi aritmatika Pembagian
hasil_bagi = a / b
print( a, " : ", b, " = ", hasil_bagi )
# operasi aritmatika PERKALIAN
hasil_Kali = a * b
print( a, " x ", b, " = ", hasil_Kali )
# operasi aritmatika SISABAGI
hasil_Modulus = a % b
print( a, " % ", b, " = ", hasil_Modulus )
# operasi aritmatika EKSPONEN (BERPANGKAT)
hasiPangkat = a ** b
print( a, " ** ", b, " = ", hasiPangkat )
# operasi aritmatika FLOW DIVISION (PEMBAGIAN NAMUN DIBULATKAN KEBAWAH)
hasilBulatBawah = a // b
print( a, " // ", b, " = ", hasilBulatBawah )

